package arrays;

public class IteratingArrays {

	public static void main(String[] args) {
						//	   0        1       2        3        4          5
		String names[] = {"Chorong", "Bomi", "Eunji", "Naeun", "Namjoo", "Hayoung"};
		int i = 2;
		
		while (i<=5) {
			System.out.println(names[i]);
			i++;
			
		}

	}

}
