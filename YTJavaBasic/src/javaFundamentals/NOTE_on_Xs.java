package javaFundamentals;

public class NOTE_on_Xs {

	public static void main(String[] args) {
		//THIS IS ABOUT X++ AND ++X
		
		// x++ po post increment tawag
		// ++x po pre increment tawag
		// Hindi po natin siya mapapansin kung sa loop lang po natin gagamitin pero po for example

		// POST increment
		// int x = 5;
		// int y = x++;

		// Ang magiging value po ni y is 5 and si x is 6

		// Pag naman po ganto

		// PRE increment
		// int x = 5;
		// int y = ++x;

		// value po ni x and y is 6

		// Meaning po ung post increment pag inassign po siya aasign muna siya bago niya dagdagan sarili niya

		// Ung pre increment po dadagdagan muna po niya sarili niya bago maassign

	}

}
